// Different Types of Comparison in JavaScript
console.log(5 > 10);
console.log(5 < 10);
console.log(5 == 10);
console.log(10 == 10);
console.log(15 >= 10);
console.log(5 >= 10);
console.log(5 <= 10);
console.log(10 <= 10);
console.log(100 >= 10);
console.log(100 <= 10);

// --- Example of Comparison ---
var orangePrice = 180;
var applePrice = 160;

console.log(orangePrice > applePrice);
console.log(orangePrice < applePrice);

/**
 * *** Difference between Double Equal (==) and Triple Equal (===) ***
 * ==
 * ===
*/
console.log(10 == "10"); // ==> Check Only Value
console.log(10 === "10"); // ==> Check Value and Type

// -------------------------
console.log(10 != "10");
console.log(10 !== "10");

